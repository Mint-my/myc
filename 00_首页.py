import streamlit as st

st.title("在星辉与灯火之间")
st.text(
    "夜深了，城市像一艘缓缓靠岸的船，灯火是它扬起的帆。"
    "我独自站在天桥上，看车流成河，看霓虹倒映在水洼里，像一面面碎裂的镜子。"
    "风从远方赶来，带着白玉兰的淡香，也带着地铁尾音的轰鸣。"
    "那一刻，我忽然想起故乡稻田里的蛙声——同样辽阔，同样不知疲倦。"
    "原来，成长只是把\"远方\"两个字，从课本搬到脚下，再从脚下，搬进心里。\n"
    "　　小时候，我把日子叠成纸船，放进名叫\"理想\"的河流。"
    "大人们说，那河流通向大海；伙伴们说，那河流遍布暗礁。"
    "浪头打来，纸船湿了边角，却并未沉没。"
    "它载着凌晨两点的灯光，载着被退回的稿纸，载着我偷偷抹去的泪，一路颠簸，却始终指向海平线上那道微光。"
    "那时我不懂，所谓\"坚持\"，并不是钢铁般的呐喊，而是湿透后仍肯继续漂浮的温柔。\n"
    "　　后来，我遇见许多人。"
    "有人递给我伞，却在下一个路口转身；有人随手撒下星子，却在我头顶撑开一整片银河。"
    "我学会在告别时微笑，把祝福说得轻飘，却在转身后把背影走得沉重。"
    "因为懂得，所以慈悲；因为不舍，所以庄重。"
    "每一次挥手，都在心里为对方种下一片春天的草地——风吹十万八千里，绿意从不声张。\n"
    "　　我把故事写进简历，也写进皱纹；把热爱写进歌，也写进沉默。"
    "世界很大，大得足以容纳所有格格不入；世界也很小，小得刚好装得下一句\"我懂你\"。"
    "于是，在深夜的便利店、在清晨的地铁闸机、在无人注意的转角，我与无数相似的灵魂擦肩，"
    "像两束频率相同的萤火，彼此照亮一瞬，然后各自奔赴漫长的黑夜。"
    "那一瞬，已足够让宇宙偷偷松了口气。\n"
    "　　如今，我仍会在暴雨里奔跑，仍会对着空白的文档发呆，仍在人声鼎沸的刹那突然感到孤独。"
    "但我也学会在阳台种薄荷，在耳机里藏一首童年的曲子，在拥挤的日程里留一段空白——"
    "像给忙碌的海洋留一处安静的湾。"
    "我开始相信，花开不是为了被看见，而是为了完成自己；就像星光赶路，不为抵达，只为照见。\n"
    "　　所以，请允许我继续向前吧。"
    "不必光芒万丈，不必众声喧哗。"
    "只需像此刻一样，把心跳调到最舒适的节奏，在星辉与灯火之间，慢慢走。"
    "风会记得我来时的方向，路会收藏我每一步的重量。"
    "而当某天回首，我会发现——\n"
    "　　所有淋过的雨，都已化作头顶的云；\n"
    "　　所有受过的伤，都已长成骨里的花；\n"
    "　　所有看似渺小的日常，都已悄悄连成一片灿烂的银河，在我心里，温柔地闪烁。\n"
    "　　那时，我将轻声对自己说：\n"
    "\"原来，我早已成为自己的光。\""
)
